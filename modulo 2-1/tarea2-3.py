
def calculadora(num1,num2,op):
    if op ==1:
        print(f"el resultado de la suma es {num1} y {num2}: {num1 + num2}")
    elif op ==2:
        print(f"el resultado de la resta es {num1} y {num2}: {num1 - num2}")
    elif op ==3:
        print(f"el resultado de la multiplicacion es {num1} y {num2}: {num1 * num2}")
    elif op ==4:
        print(f"el resultado de la divicion es {num1} y {num2}: {num1 / num2}")
    else:
        print("intentalo de nuevo")


calculadora (10,10,1)
